 <?php

/**
 * Adição +
 * Subtração -
 * Multiplicação *
 * Módulo %
 * Exponenciação **
 */

 $a = 10;
 $b = 20;
 $c = 30;
 $d = 5;

 //Soma
 echo $a + $b;

 //Subtração
echo $b - $c;

//Multiplicação
echo $d * $c;

//Módulo
echo $b % $d;


//Expoenciação
echo $a ** $d;